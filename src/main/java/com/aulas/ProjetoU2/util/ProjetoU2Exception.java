package com.aulas.ProjetoU2.util;


public class ProjetoU2Exception extends Exception {
	
	public ProjetoU2Exception() {
		super();
	}
	
	public ProjetoU2Exception(String msg, Throwable thr) {
		super(msg,thr);
	}
	
	public ProjetoU2Exception(String msg) {
		super(msg);
	}
	
	public ProjetoU2Exception(Throwable thr) {
		super(thr);
	}

}
